const convertUnder100 = (num: number): string => {
    if (num < 10) return units[num];
    if (num >= 10 && num < 20) return teens[num - 10];
    if (fullNumbers[num]) return fullNumbers[num];
    const unitPart = num % 10;
    const tenPart = Math.floor(num / 10);
    return tens[tenPart] + (unitPart > 0 ? units[unitPart] : '');
};

export const numberToMarathiWords = (num: number): string => {
    if (num === 0) return 'शून्य';
    if (num < 0) return 'उणे ' + numberToMarathiWords(Math.abs(num));

    let result = '';

    // Crores
    if (num >= 10000000) {
        result += numberToMarathiWords(Math.floor(num / 10000000)) + ' कोटी ';
        num %= 10000000;
    }

    // Lakhs
    if (num >= 100000) {
        result += convertUnder100(Math.floor(num / 100000)) + ' लाख ';
        num %= 100000;
    }

    // Thousands
    if (num >= 1000) {
        result += convertUnder100(Math.floor(num / 1000)) + ' हजार ';
        num %= 1000;
    }

    // Hundreds
    if (num >= 100) {
        const h = Math.floor(num / 100);
        if (h === 1) result += 'शंभर ';
        else result += units[h] + 'शे ';
        num %= 100;
    }

    // Under 100
    if (num > 0) {
        result += convertUnder100(num);
    }

    return result.trim();
};
